package com.DataType_1;
/**
 * data types -> 1. primitive and 2.  non-primitive
 * 1. Primitive : Building block (basic data types)
 * Total 8 primitive data types : 1.byte(1 byte) 2.short(2) 3.int(4) 4.float(8)
 * 5.double(8) 6.long(8) 7.boolean(1) 8.char(2)
 * */
public class DataType {

    public static void main(String[] args)
    {
        byte a=10;
        short b=1234;
        int c=1234567;
        long d=1234567896;
        float e=4563.123453f;               //all floats are double so we need to convert
        double f=12342345.67892d;
        boolean g=true;
        char h='x';

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);

    }
}
